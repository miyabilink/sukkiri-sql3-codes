-- 04-01
SELECT DISTINCT 費目 FROM 家計簿;

-- 04-02
SELECT * FROM 家計簿
 WHERE 日付 >= '2018-03-01'
   AND 日付 <= '2018-03-31'
 ORDER BY 出金額 DESC;

-- 04-03
SELECT * FROM 家計簿
 WHERE 費目 = '給料' ORDER BY 入金額 DESC
OFFSET 0 ROWS FETCH NEXT 5 ROWS ONLY;

-- 04-04
SELECT * FROM 家計簿
 UNION
SELECT * FROM 家計簿アーカイブ
 WHERE 日付 >= '2018-02-01'
   AND 日付 <= '2018-02-28'
 ORDER BY 1;

-- 04-05
SELECT 費目 FROM 家計簿
EXCEPT
SELECT 費目 FROM 家計簿アーカイブ;
